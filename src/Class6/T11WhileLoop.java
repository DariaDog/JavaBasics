package Class6;

public class T11WhileLoop {
    public static void main(String[] args) {


        int i=21;
        do{
            System.out.print(i+" ");
            i++;
            i++;
        }while (i<=49);

        System.out.println();

        for (int v=21; v<=49; v+=2){
            System.out.print(v+" ");
        }

    }}

