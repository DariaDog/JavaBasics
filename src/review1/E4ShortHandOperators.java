package review1;

public class E4ShortHandOperators {
    public static void main(String[] args) {

        int age = 10;
        age += 20; // adding 20 more to the value of age variable
        System.out.println(age);

        // shortcut to format the code Ctrl+Alt+L   Cmd+Ops+L

        int age1 = 10;
        // age *= 2;
        age1 = age1 * 2;
        System.out.println(age1);


    }
}
